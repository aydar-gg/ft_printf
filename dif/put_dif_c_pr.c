#include "header.h"

void	put_dif_c_pr(int *counter)
{
	if (counter[7] && counter[2])
	{
		while (counter[2] - 1)
		{
			ft_putchar_fd(' ', 1, counter);
			counter[2]--;
		}
	}
}
