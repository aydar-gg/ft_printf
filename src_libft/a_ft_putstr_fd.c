#include "header.h"

void	ft_putstr_fd(char *s, int fd, int *counter)
{
	int i;

	i = 0;
	if (s && fd)
	{
		while (s[i] != '\0')
		{
			i++;
			counter[3]++;
		}
		write(fd, s, i);
	}
}
